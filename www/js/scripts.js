function initMap() {
    bounds  = new google.maps.LatLngBounds();

    var map = new google.maps.Map(document.getElementById('map-canvas'), {
        zoom: 15,
        center: coord_church,
        scrollwheel: false
    });

    createMarker(map, desc_church, coord_church);
    createMarker(map, desc_rist, coord_rist);
    createMarker(map, desc_sposa, coord_sposa);
    createMarker(map, desc_sposo, coord_sposo);

    for (i = 0; i < markers.length; ++i) {
        loc = new google.maps.LatLng(markers[i].position.lat(), markers[i].position.lng());
        bounds.extend(loc);
    }
     
    map.fitBounds(bounds);
    map.panToBounds(bounds);
}

function createMarker(map, desc, coord){
    var marker = new google.maps.Marker({
        position: coord,
        map: map,
        title: desc
    });
    var infowindow = new google.maps.InfoWindow({
        content: desc
    });
    marker.addListener('click', function() {
        for (i = 0; i < infowindows.length; ++i) {
            if (infowindows[i]) {
                console.log("entro");
                infowindows[i].close();
            }
        }
        infowindow.open(map, marker);
    });

    markers.push(marker);
    infowindows.push(infowindow);

    return marker;
}

